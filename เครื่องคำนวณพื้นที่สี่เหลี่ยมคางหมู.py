# โปรแกรมคำนวณพื้นที่สี่เหลี่ยมคางหมู

print("=== โปรแกรมคำนวณพื้นที่สี่เหลี่ยมคางหมู ===")

base1 = float(input("กรอกความยาวฐานบน: "))
base2 = float(input("กรอกความยาวฐานล่าง: "))
height = float(input("กรอกความสูง: "))

area = ((base1 + base2) * height) / 2

print(f"\nพื้นที่สี่เหลี่ยมคางหมู = {area:.2f} ตารางหน่วย")